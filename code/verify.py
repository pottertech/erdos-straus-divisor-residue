"""
Verification of key theorems in the Erdős–Straus divisor-residue manuscript.

Tests:
1. Theorem 1: T ∈ D_A iff A works (divisor-residue criterion)
2. Theorem 2: n ≡ 5 (mod 8) → A = 3 works
3. Theorem 3: T ∈ D_A iff QNR exists (converse proven, forward computational)
4. Theorem 5: Legendre symbol identity (p/A) = (n/p)
5. Bounded Divisor-Residue Lemma: -1 ∈ H(A) ⟹ -1 ∈ D_A
6. D_A vs H(A): -1 membership equivalence
7. D_A closure: shifted divisor-residue set D_A^{(nm)}
8. Theorem 6: A ≤ 31 covers all n ≤ 100,000
"""

import json
from sympy import factorint, isprime, gcd, legendre_symbol, primitive_root
from math import lcm
from functools import reduce
import itertools
import sys

def find_solution(n, A):
    """Find Erdős–Straus solution for given n, A. Returns (x, y, z) or None."""
    if (n + A) % 4 != 0:
        return None
    x = (n + A) // 4
    m = (n + A) // 4
    nx = n * m
    nx_factors = factorint(nx)
    
    T = (-n * m) % A  # target residue = -n²·4⁻¹ mod A
    
    # Search for divisor P of (nx)² with P ≡ T (mod A)
    # P = ∏ p_i^{α_i} with 0 ≤ α_i ≤ 2*v_{p_i}(nx)
    primes = list(nx_factors.keys())
    max_exps = [2 * nx_factors[p] for p in primes]
    
    for combo in itertools.product(*[range(e + 1) for e in max_exps]):
        P = 1
        for p, a in zip(primes, combo):
            P *= p ** a
        if P % A == T % A:
            Q = nx * nx // P
            if Q == 0:
                continue
            y_num = P + nx
            z_num = Q + nx
            if y_num % A == 0 and z_num % A == 0:
                y = y_num // A
                z = z_num // A
                if y > 0 and z > 0:
                    return (x, y, z)
    return None


def compute_D_A(n, A, tight=False):
    """Compute bounded divisor-residue set D_A((nx)²)."""
    m = (n + A) // 4
    nx = n * m
    nx_factors = factorint(nx)
    
    bound = lambda e: e if tight else 2 * e
    
    res = {1}
    for p, e in nx_factors.items():
        if gcd(p, A) > 1:
            continue
        new = set()
        for f in range(bound(e) + 1):
            pf = pow(p, f, A)
            for r in res:
                new.add((r * pf) % A)
        res = new
    return res


def compute_H_A(n, A):
    """Compute subgroup H(A) generated by prime factors of nx."""
    m = (n + A) // 4
    nx = n * m
    nx_factors = factorint(nx)
    
    gens = {p % A for p in nx_factors if gcd(p, A) == 1}
    H = {1}
    changed = True
    while changed:
        changed = False
        new = set()
        for g in gens:
            for s in H:
                v = (s * g) % A
                if v not in H:
                    new.add(v)
                    changed = True
        H |= new
    return H


def has_qnr(n, A):
    """Check if any prime factor of nx is QNR mod A."""
    m = (n + A) // 4
    nx = n * m
    nx_factors = factorint(nx)
    
    for p in nx_factors:
        if gcd(p, A) > 1:
            continue
        d = 1
        v = p % A
        c = v
        while c != 1 and d < A:
            c = (c * v) % A
            d += 1
        if d % 2 == 0:
            return True
    return False


def verify_theorem1(cases):
    """T ∈ D_A iff A works."""
    mismatches = 0
    for n, A in cases:
        D = compute_D_A(n, A)
        T = (-n * ((n + A) // 4)) % A
        sol = find_solution(n, A)
        T_in = T in D
        works = sol is not None
        if T_in != works:
            mismatches += 1
    return mismatches


def verify_theorem2():
    """n ≡ 5 (mod 8) → A = 3 works."""
    failures = 0
    for n in range(5, 10001, 8):  # n ≡ 5 mod 8
        if not isprime(n):
            continue
        if gcd(n, 3) > 1:
            continue
        sol = find_solution(n, 3)
        if sol is None:
            failures += 1
            print(f"  Theorem 2 FAIL: n={n}")
    return failures


def verify_theorem3(cases):
    """T ∈ D_A iff QNR exists."""
    mismatches = 0
    for n, A in cases:
        D = compute_D_A(n, A)
        T = (-n * ((n + A) // 4)) % A
        qnr = has_qnr(n, A)
        T_in = T in D
        if T_in != qnr:
            mismatches += 1
    return mismatches


def verify_theorem5(cases):
    """(p/A) = (n/p) for p | m_A."""
    mismatches = 0
    for n, A in cases:
        m = (n + A) // 4
        m_factors = factorint(m)
        for p in m_factors:
            if gcd(p, A) > 1 or gcd(p, n) > 1:
                continue
            lhs = int(legendre_symbol(p, A))
            rhs = int(legendre_symbol(n, p))
            if lhs != rhs:
                mismatches += 1
    return mismatches


def verify_lemma(cases):
    """-1 ∈ H(A) ⟹ -1 ∈ D_A."""
    failures = 0
    for n, A in cases:
        H = compute_H_A(n, A)
        D = compute_D_A(n, A)
        neg1 = A - 1
        neg1_in_H = neg1 in H
        neg1_in_D = neg1 in D
        if neg1_in_H and not neg1_in_D:
            failures += 1
    return failures


def verify_DA_HA_membership(cases):
    """-1 ∈ D_A iff -1 ∈ H(A)."""
    mismatches = 0
    for n, A in cases:
        H = compute_H_A(n, A)
        D = compute_D_A(n, A)
        neg1 = A - 1
        if (neg1 in D) != (neg1 in H):
            mismatches += 1
    return mismatches


def verify_shifted_set(cases):
    """Check D_A^{(nm)}: -1 in shifted set vs T in D_A."""
    shifted_failures = 0
    T_in_DA_count = 0
    for n, A in cases:
        D_full = compute_D_A(n, A, tight=False)
        D_tight = compute_D_A(n, A, tight=True)
        T = (-n * ((n + A) // 4)) % A
        neg1 = A - 1
        
        T_in = T in D_full
        if T_in:
            T_in_DA_count += 1
        
        # -1 in shifted set (tight bound = v_p(nx))
        neg1_in_shifted = neg1 in D_tight
        neg1_in_full = neg1 in D_full
        
        if neg1_in_full and not neg1_in_shifted:
            shifted_failures += 1
    
    return shifted_failures, T_in_DA_count


