    # Prime-A cases only for some tests
    prime_A_cases = [(n, A) for n, A in cases if isprime(A)]
    
    print(f"\nTest cases: {len(cases)} total, {len(prime_A_cases)} prime-A")
    
    # Theorem 2
    print("\n--- Theorem 2: n ≡ 5 (mod 8) → A = 3 works ---")
    fails = verify_theorem2()
    print(f"  Failures: {fails}")
    assert fails == 0, "Theorem 2 has failures!"
    
    # Theorem 5
    print("\n--- Theorem 5: (p/A) = (n/p) for p | m_A ---")
